client.elevation = message => {
    if (!message.guild) {
        return;
    }
    let permlvl = 0;
    if (message.member.hasPermission("MOVE_MEMBERS")) permlvl = 1;
    if (message.member.hasPermission("VIEW_AUDIT_LOG")) permlvl = 2;
    if (message.member.hasPermission("ADMINISTRATOR")) permlvl = 3;
    if (message.author.id === config.own) permlvl = 4;
    return permlvl;
};

